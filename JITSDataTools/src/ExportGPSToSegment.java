    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        // Get index Segments to process
        int fromSeg = Integer.parseInt(jTextField2.getText().split("-")[0]);
        int toSeg = Integer.parseInt(jTextField2.getText().split("-")[1]);
        
        File directory = new File(jTextField4.getText());     
        File[] files = directory.listFiles();
        
        GPSFormatter gpsFormatter = new GPSFormatter(jTextField5.getText(), jTextField6.getText()
                                    , jTextField7.getText(), jTextField8.getText()
                                    , jTextField9.getText(), jTextField10.getText()
                                    , jTextField11.getText(),jTextField12.getText(),jTextField13.getText());
            
            double radiusAroundSegment = Double.parseDouble(jTextField14.getText()); // in metter
            
            if(jCheckBox1.isSelected()){
                // Scan all segment in listSegment
                for(int i = fromSeg; i < toSeg; i++){
                    
                    Segment seg = listSegment.get(i);
                    
                    System.out.println(Parameters.ROOT_PATH_SEGMENT_DATA+"\\"+String.valueOf(radiusAroundSegment)+"\\"+seg.getSegment_id());
                    
                    // Create folder to save data for segment
                    File dirSegmentData = new File(Parameters.ROOT_PATH_SEGMENT_DATA+"\\"+String.valueOf(radiusAroundSegment)+"\\"+seg.getSegment_id());
                    if (!dirSegmentData.exists()) {
                        if (dirSegmentData.mkdirs()) {
                            System.out.println("Directories is created!");
                        } else {
                            System.out.println("Failed to create directories!");
                        }                      
                    }
                    
                    // Process all files in directory
                    for(File f : files){          
                        try {
                            // Create file to store data segment                           
                            System.out.println(dirSegmentData.getAbsolutePath()+"\\"+f.getName().split("\\.")[0]+".csv");
                            
                            FileWriter fw = new FileWriter(dirSegmentData.getAbsolutePath()+"\\"+f.getName().split("\\.")[0]+".csv");
                            
                            
                            BufferedWriter bw = new BufferedWriter(fw);
                          
                               
                            // Read data from raw file
                            FileReader fr = new FileReader(f);
                            BufferedReader br = new BufferedReader(fr);
                            // Get List distinct date
                            String line; // remove header
                            while ((line = br.readLine()) != null) {
                                GPS gps = new GPS(line, gpsFormatter);
                                Point point = new Point(gps.getxLatitude(), gps.getyLongitute());
                                if(seg.isBelongSegment(point, radiusAroundSegment/1000.0)){
                                    bw.write(line);
                                    bw.newLine();
                                }
                            }
                            br.close();
                            fr.close(); 
                            
                            bw.close();
                            fw.close();

                        } catch (FileNotFoundException ex) {
                            //Logger.getLogger(ExportGPSToSegment.class.getName()).log(Level.SEVERE, null, ex);
                            System.out.println(ex.getMessage());
                        } catch (IOException ex) {
                            //Logger.getLogger(ExportGPSToSegment.class.getName()).log(Level.SEVERE, null, ex);
                            System.out.println(ex.getMessage());
                        }  
                        System.out.println("Finish file "+f.getName());
                    }
                    
                }
            }    
    }//GEN-LAST:event_jButton3ActionPerformed

