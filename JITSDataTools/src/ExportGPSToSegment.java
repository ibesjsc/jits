    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        int returnVal = fc.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            jTextField1.setText(file.getAbsolutePath());
                       
            // Create object of FileReader
            try{
                SegmentFormatter segFormatter = new SegmentFormatter(jTextField15.getText(), jTextField16.getText()
                                    , jTextField17.getText(), jTextField18.getText()
                                    , jTextField19.getText(), jTextField20.getText()
                                    , jTextField21.getText(),jTextField22.getText());
                
                FileReader inputFile = new FileReader(file);

                // Instantiate the BufferedReader Class
                BufferedReader bufferReader = new BufferedReader(inputFile);

                // Variable to hold the one line data
                String line = bufferReader.readLine(); // remove header
                while ((line = bufferReader.readLine()) != null) {
                        if (!line.isEmpty()) {
                            try{                              
                                Segment seg = new Segment(line, segFormatter);
                                listSegment.add(seg);
                            }
                            catch(Exception e){
                                //LogFile.writeLog(e.getMessage());
                                //System.out.println(line+": "+e.getMessage());
                            }
                        }
                }
                bufferReader.close();
                jLabel3.setText(listSegment.size()+" valid segments");
            }
            catch(Exception ex){
                // LogFile.writeLog(ex.getMessage());
                   System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

